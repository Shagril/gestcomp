<h1>Accueil - Professeur</h1>

<pre>Chaque titre d'un sous-menu héberge un lien vers une rubrique d'aide contextuelle

<span style="font-size:20px"><b>Promotion</b></span>

Les promotions que vous suivez apparaissent dans la liste déroulante. Le choix de l'une d'entre elles vous permet de consulter les travaux de tous les étudiants concernés.

<span style="font-size:20px"><b>Suivi</b></span>

Vous pouvez consulter puis commenter les situations enregistrées par les étudiants de votre promotion.

Selon leur emplacement, réseau local par exemple, les ressources dont l'adresse est indiquée par l'étudiant ne sont pas toujours accessibles pour le professeur.

<span style="font-size:20px"><b>Données</b></span>

Vous pouvez observer le tableau de synthèse d'un étudiant sélectionné ou son tableau bilan.

<span style="font-size:20px"><b>Divers</b></span>

Vous pouvez restaurer l'ensemble des données (situations, commentaires, etc.) sauvegardées par un étudiant, par exemple après un effacement accidentel. Il appartient 
à chaque étudiant de réaliser cette sauvegarde et il doit vous fournir le fichier correspondant. La sauvegarde n'est pas incrémentielle et reconstitue les données de 
l'étudiant telles qu'à la date de la sauvegarde. Cette fonction est aussi accessible à l'administrateur de l'application.

Vous pouvez aussi modifier les caractéristiques de votre compte, votre mot de passe par exemple.</pre>