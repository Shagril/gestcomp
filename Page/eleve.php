<h1>Accueil - Eleve</h1>
<pre>
<h3>Situations</h3>
Vous saisissez de nouvelles situations ou gérez celles déjà saisies. Le libellé court d'une situation sert d'identifiant dans les interfaces où elle doit être sélectionnée ainsi que dans le tableau de synthèse (à choisir donc avec pertinence).
<h3>Synthèse</h3>
Le bilan individuel vous permet d'observer, parmi toutes les activités et compétences du référentiel, celles qui sont citées dans vos situations.
Vous pouvez aussi construire, puis sauvegarder ou imprimer, le tableau de synthèse au format PDF. Le numéro d'examen figurant en en-tête sera fourni par la convocation et devra être initialisé dans les données personnelles (voir ci-dessous).
<h3>Divers</h3>
Vous devez sauvegarder régulièrement vos données sur un support personnel (clé USB par exemple) ; une éventuelle restauration sera réalisée par un de vos professeurs.
Vous pouvez aussi modifier vos informations personnelles, mot de passe par exemple.
